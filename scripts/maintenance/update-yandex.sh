    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —Ä–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è —Å–æ–∑–¥–∞–Ω–∞ —É—Å–ø–µ—à–Ω–æ
    if [ -f "$BACKUP_NAME" ]; then
        SIZE=$(stat -c%s "$BACKUP_NAME" 2>/dev/null || stat -f%z "$BACKUP_NAME")
        echo "‚úÖ –†–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è —Å–æ–∑–¥–∞–Ω–∞: $BACKUP_NAME (—Ä–∞–∑–º–µ—Ä: $SIZE –±–∞–π—Ç)"
        
